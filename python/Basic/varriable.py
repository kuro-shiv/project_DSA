# Definition of a variable:
# A variable is a container used to store data values that can be changed and used later in the program.

# String variable
first_name = "Bro"  # stores text
print(f"Hello {first_name}")

# Integer variable
age = 25  # stores whole numbers
print(f"I am {age} years old")

# Float variable
height = 5.9  # stores decimal numbers
print(f"My height is {height} feet")

# Boolean variable
is_student = True  # stores True or False
print(f"Am I a student? {is_student}")

# Multiple assignment
x, y, z = 1, 2, 3  # assign multiple values at once
print(x, y, z)
print(x, y)
print(x)

# Updating variable
score = 10  # initial value
score = score + 5  # updated value
print(score)

# Variable with underscore
my_variable = "Python"  # valid variable name with underscore
print(my_variable)

# Variable case sensitivity
Age = 30
age = 25
print(Age)  # 30
print(age)  # 25
